package day6_Arrays;

public class ArraysDemo9 {

	public static void main(String[] args) {
		
		int number[] = new int[6];
		
		number[0] = 100;
		number[1] = 200;
		number[2] = 300;
		number[3] = 400;
		number[4] = 500;
		number[5] = 600;
		
		for (int a=5; a>=0 ; a--)
		{
			System.out.println(number[a]);
		}
		
	}

}
